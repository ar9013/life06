package com.ar9013.life06;

import com.badlogic.gdx.ApplicationAdapter;
import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.GL20;
import com.badlogic.gdx.graphics.Texture;
import com.badlogic.gdx.graphics.g2d.SpriteBatch;

import java.util.Random;

public class Life06 extends ApplicationAdapter {

	SpriteBatch batch;
	Texture img;
	
	@Override
	public void create () {
		batch = new SpriteBatch();
		img = new Texture("badlogic.jpg");
	}

	@Override
	public void render () {
		Gdx.gl.glClearColor(new Random().nextFloat(), new Random().nextFloat(),new Random().nextFloat(),1 ); // 透過 隨機取得值 設定要改變的顏色
		Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT); // 改變顏色
		batch.begin();
		batch.draw(img, 0, 0);
		batch.end();
	}
	
	@Override
	public void dispose () {
		batch.dispose();
		img.dispose();
	}
}
